import type { Question, QuestionOption } from "@/types/risk";

const opt = (
  label: string,
  score: number,
  riskLevel: QuestionOption["riskLevel"],
  riskReason: string,
  suggestion: string,
  employeeLabel?: string,
  legalBasis?: string,
): QuestionOption => ({ label, employeeLabel, score, riskLevel, riskReason, suggestion, legalBasis });

const yes = (
  suggestion = "保留现有材料，并整理成可提交、可追溯的证据目录。",
  employeeLabel?: string,
  legalBasis?: string,
) => opt("是，已有明确材料", 0, "low", "该事项已有基础证据支撑，当前缺口较小。", suggestion, employeeLabel || "是，有相关材料", legalBasis);

const partial = (riskReason: string, suggestion: string, employeeLabel?: string, legalBasis?: string) =>
  opt("部分具备，但不完整", 5, "medium", riskReason, suggestion, employeeLabel || "部分有，但不完整", legalBasis);

const unsure = (riskReason: string, suggestion: string, employeeLabel?: string, legalBasis?: string) =>
  opt("不确定，需要核查", 8, "medium", riskReason, suggestion, employeeLabel || "不确定，需要核查", legalBasis);

const no = (riskReason: string, suggestion: string, score = 10, employeeLabel?: string, legalBasis?: string) =>
  opt("否，暂未形成材料", score, score >= 25 ? "critical" : "high", riskReason, suggestion, employeeLabel || "否，没有", legalBasis);

